def simon_says_module():
    """Модуль 'Делай как я' (Simon Says)"""
    print("\n" + "=" * 50)
    print("МОДУЛЬ: ДЕЛАЙ КАК Я")
    print("=" * 50)
    
    # Определяем наличие гласных в серийном номере
    has_vowel = input("\nЕсть ли в серийном номере гласная (A, E, I, O, U)? (да/нет): ").strip().lower()
    
    # Количество ошибок
    while True:
        try:
            strikes = int(input("Сколько ошибок сейчас на бомбе (0-2)? "))
            if 0 <= strikes <= 2:
                break
            print("Должно быть от 0 до 2!")
        except ValueError:
            print("Введите число!")
    
    # Таблицы преобразования
    if has_vowel == 'да':
        tables = {
            0: {'кр': 'синий', 'с': 'красный', 'з': 'желтый', 'ж': 'зеленый'},
            1: {'кр': 'желтый', 'с': 'зеленый', 'з': 'синий', 'ж': 'красный'},
            2: {'кр': 'зеленый', 'с': 'красный', 'з': 'желтый', 'ж': 'синий'}
        }
    else:
        tables = {
            0: {'кр': 'синий', 'с': 'желтый', 'з': 'зеленый', 'ж': 'красный'},
            1: {'кр': 'красный', 'с': 'синий', 'з': 'желтый', 'ж': 'зеленый'},
            2: {'кр': 'желтый', 'с': 'зеленый', 'з': 'синий', 'ж': 'красный'}
        }
    
    current_table = tables[strikes]
    sequence = []
    
    print("\n" + "=" * 50)
    print("ПОСЛЕДОВАТЕЛЬНОСТЬ")
    print("=" * 50)
    print("\nВводите цвета по мере их появления.")
    print("Доступные цвета: красный, синий, зеленый, желтый")
    print("Для просмотра полной последовательности введите 'показать'")
    print("Для возврата в меню введите 'выход'")
    print()
    
    step = 1
    while True:
        color = input(f"Шаг {step} - светится: ").strip().lower()
        
        if color == 'выход':
            break
        elif color == 'показать':
            if sequence:
                print("\n" + "=" * 50)
                print("ПОЛНАЯ ПОСЛЕДОВАТЕЛЬНОСТЬ ДЛЯ НАЖАТИЯ:")
                for i, c in enumerate(sequence, 1):
                    print(f"{i}. {c}")
                print("=" * 50)
                print()
            else:
                print("Последовательность пуста!\n")
        elif color in ['кр', 'с', 'з', 'ж']:
            press_color = current_table[color]
            sequence.append(press_color)
            print(f"  → Нажмите: {press_color}")
            print(f"\nТекущая последовательность: {' → '.join(sequence)}\n")
            step += 1
        else:
            print("Неверный цвет! Используйте: красный(кр), синий(с), зеленый(з), желтый(ж)\n")
    
    input("\nНажмите Enter для возврата в меню...")